        <h3>ROOT CANAL TREATMENT</h3>
        <p>The pulp is the living tissue of the tooth with blood supply and nerve supply. Once the dental caries (decay ) involves the pulp, the pulp gets infected causing pain. The aim of the root canal treatment is to remove the infected pulp. This is done by removing the infected pulp with files in the pulp chamber and cleaning and shaping the root canals and sealing the canal with a filling material.</p>
        <h3>THE STORY OF ROOT CANAL THERAPY</h3>
        <p>Each tooth has a soft tissue – the pulp which nourishes the tooth. Because of deep decay, injury, or gum disease, the pulp tissue in your tooth has become inflamed or infected. In any other part of your body, if a similar tissue becomes diseased, the body merely throws it off and forms new tissue. However, a tooth is a unique and different. Because the infected soft tissue (pulp) within the tooth is totally encased within hard tissue, it is the role of the dentist to remove the soft tissue located in the root canals, cleanse the area, and finally fill the canals with a special material so that bacteria cannot re-enter the tooth to cause another infection. When the endodontic treatment is complete, the tooth is by no means "dead". It receives quite adequate support from the surrounding tissues and may be expected to last as long as any other natural tooth.</p>
